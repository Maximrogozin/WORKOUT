const update = (id, data) =>
  new Promise((resolve) => {
    const allCatalog = JSON.parse(localStorage.getItem("allCatalog"));
    const userIndex = allCatalog.findIndex((u) => u._id === id);
    allCatalog[userIndex] = {
      ...allCatalog[userIndex],
      ...data,
    };
    localStorage.setItem("allCatalog", JSON.stringify(allCatalog));
    resolve(allCatalog[userIndex]);
  });

